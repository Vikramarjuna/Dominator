func hasHttpAnnotations(methods []MethodInfo) bool {
	for _, method := range methods {
		if method.HttpMethod != "" {
			return true
		}
	}
	return false
}

func getPackageNameFromFile(filename string) string {
	// Extract package name from filename
	// e.g., "/path/to/hypervisor.proto" -> "hypervisor"
	parts := strings.Split(filename, "/")
	if len(parts) == 0 {
		return "unknown"
	}
	name := parts[len(parts)-1]
	return strings.TrimSuffix(name, ".proto")
}

